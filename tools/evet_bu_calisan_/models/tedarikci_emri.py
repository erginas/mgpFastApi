    firma: Mapped[Optional['Firma']] = relationship(back_populates='tedarikci_emri')
    kisi: Mapped[Optional['Kisi']] = relationship(back_populates='tedarikci_emri')
    kisi_1: Mapped[Optional['Kisi']] = relationship(back_populates='tedarikci_emri')
    kisi_1_2: Mapped[Optional['Kisi']] = relationship(back_populates='tedarikci_emri')
    kisi_1_2_3: Mapped[Optional['Kisi']] = relationship(back_populates='tedarikci_emri')