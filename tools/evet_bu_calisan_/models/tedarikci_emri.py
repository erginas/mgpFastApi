    siparis_ay: Mapped[Optional[Float]] = mapped_column(Float)
    siparis_yil: Mapped[Optional[Float]] = mapped_column(Float)
    siparis_no: Mapped[Optional[Float]] = mapped_column(Float)
    irsaliye_no: Mapped[Optional[String]] = mapped_column(String)
    sertifia_no: Mapped[Optional[String]] = mapped_column(String)
    durumu: Mapped[Optional[String]] = mapped_column(String)
