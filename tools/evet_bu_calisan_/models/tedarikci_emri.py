    istenen_termin: Mapped[Optional[DateTime]] = mapped_column(DateTime)
    termin_t: Mapped[Optional[DateTime]] = mapped_column(DateTime)
    yollama_t: Mapped[Optional[DateTime]] = mapped_column(DateTime)
    donus_t: Mapped[Optional[DateTime]] = mapped_column(DateTime)
    aciklama: Mapped[Optional[String]] = mapped_column(String)
    iptal_tarihi: Mapped[Optional[DateTime]] = mapped_column(DateTime)
    onerilen_termin: Mapped[Optional[DateTime]] = mapped_column(DateTime)
    fason_ay: Mapped[Optional[Float]] = mapped_column(Float, nullable=False)
    emir_tipi: Mapped[Optional[String]] = mapped_column(String)
    siparis_ref_no: Mapped[Optional[String]] = mapped_column(String)
    kapandi_fl: Mapped[Optional[String]] = mapped_column(String)
    lot_fl: Mapped[Optional[String]] = mapped_column(String)
    iptal_eden: Mapped[Optional[Float]] = mapped_column(Float)
